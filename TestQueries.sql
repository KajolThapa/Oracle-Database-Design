     SCENARIO

*/

SELECT FLIGHT.FlightNumber, FLIGHT.Destination, FLIGHT.FlightTime, FLIGHT_ARRIVAL_AIRPORT.AirportCode, 
AIRPORT.AirportName, AIRLINE.AirlineName, AIRLINE.HeadquartersAddress
FROM FLIGHT 
JOIN FLIGHT_ARRIVAL_AIRPORT  ON FLIGHT.Flightnumber = FLIGHT_ARRIVAL_AIRPORT.Flightnumber
JOIN AIRPORT on AIRPORT.airportcode = flight_arrival_airport.airportcode
JOIN AIRLINE ON flight.airlinecode = airline.airlinecode
WHERE FLIGHT.Destination = 'London';


